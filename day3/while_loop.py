#!/usr/bin/env python

# loops, There are 2 kinds of loops:
# while loop and for loop
# To execute a code multiple times(suppose you want to print "hello world" 10 times), we use loops.
# while loop is always same in all the programming languages
# But for loop may differ as per the different programming languages

# IMP: whatever we can do using while loop, can be done using for loop and vice-versa

# we want to print "Hello World" 10 times:

i = 1
while i<=10:
    print("Hello World")
    i = i + 1
