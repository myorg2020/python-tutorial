#!/usr/bin/env python

# Using python as calulator
# Make sure to execute like this using "python3 calc.py" rather just ./calc.py

print(2+3)
print(2+3*4)

# floating divison - means you always get point (.) in output like 2.0 and 0.5
print(2/4)
print(4/2)

# Integer divison - means you don't get point (.) in output. you will get like: 2.0 as 2 and 0.5 as 0
print(4//2)
print(2//4)

# Exponent - 2 to the power 3 is 8, in python it can be used like:
# double multiply sign means power
print(2**3)

# Using underroot in python
print(2**0.5)
print(4**0.5)

# print(2**0.5) i.e. underroot of 2 give 1.4142135623730951
# Suppose if we want to round off upto 4 values after decimal, use round function
print(round(2**0.5, 4))
# output would be 1.4142

# modulo (%) gives remainder, e.g: 3/2 = 1.5, 1 is remainder here
print(3%2)
# output is 1

# Python uses Precedence rule and associative rule to do calculation.
# As in maths we have BODMAS rule here we have Precedence and associative - just deciding the priority if caluculations
