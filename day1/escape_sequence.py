#!/usr/bin/env python

print("hello \"world\" world")
print('I\'m Amitesh')
# \n is for new line
print("line A\nline B \n line C")
# \t is for tab
print("name\tAmitesh")
# \\ (double backslash) means single backslash (\)
print("this is backslash\\")
print("this is double backslash\\\\")
# \b is used as backspace, means it deletes 1 l from below
print("hell\blo")
